namespace Z80.Net;

/// <summary>
/// Specifies one of the available interrupt modes.
/// </summary>
public enum InterruptMode
{
    /// <summary>
    /// The IM 0 interrupt mode.
    /// </summary>
    Mode0 = 0,

    /// <summary>
    /// The IM 1 interrupt mode.
    /// </summary>
    Mode1 = 1,

    /// <summary>
    /// The IM 2 interrupt mode.
    /// </summary>
    Mode2 = 2,
}